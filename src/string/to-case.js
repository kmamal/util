
const toLowerCase = (str) => str.toLowerCase()
const toUpperCase = (str) => str.toUpperCase()
const toLocaleLowerCase = (str, locale) => str.toLocaleLowerCase(locale)
const toLocaleUpperCase = (str, locale) => str.toLocaleUpperCase(locale)

module.exports = {
	toLowerCase,
	toUpperCase,
	toLocaleLowerCase,
	toLocaleUpperCase,
}
